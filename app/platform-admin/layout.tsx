function getInitials(name: string): string {
  return name
    .split(" ")
    .map((n) => n[0])
    .join("")
    .toUpperCase()
    .slice(0, 2);
}

function AdminSidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const { expert, loading, signOut } = useExpert();
  const [showDropdown, setShowDropdown] = useState(false);

  if (pathname === "/platform-admin/login") return null;

  return (
    <aside className="w-64 bg-navy-dark border-r border-gray-800 flex flex-col min-h-screen shrink-0">
      {/* Logo */}
      <div className="px-5 py-5 border-b border-gray-800">
        <Link href="/platform-admin" className="flex items-center gap-2">
          <span className="text-xl font-bold tracking-tight">
            Handy<span className="text-teal">.</span>
          </span>
          <span className="px-2 py-0.5 rounded text-[10px] font-bold uppercase tracking-wider bg-purple/15 text-purple-light border border-purple/30">
            Platform
          </span>
        </Link>
      </div>

      {/* Nav */}
      <nav className="flex-1 px-3 py-4 space-y-1">
        {NAV_ITEMS.map((item) => {
          const isActive =
            item.href === "/platform-admin"
              ? pathname === "/platform-admin"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? "bg-purple/10 text-purple-light"
                  : "text-gray-400 hover:text-white hover:bg-white/5"
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* User section */}
      <div className="border-t border-gray-800 px-3 py-4">
        {loading ? (
          <div className="flex items-center gap-3 px-3">
            <div className="w-8 h-8 rounded-full bg-gray-700 animate-pulse" />
            <div className="h-4 w-24 bg-gray-700 rounded animate-pulse" />
          </div>
        ) : (
          <div className="relative">
            <button
              onClick={() => setShowDropdown(!showDropdown)}
              className="flex items-center gap-3 px-3 py-2 rounded-lg hover:bg-white/5 transition-colors w-full text-left"
            >
              <div className="w-8 h-8 rounded-full bg-purple/20 border border-purple/30 flex items-center justify-center text-purple-light text-xs font-bold shrink-0">
                {expert ? getInitials(expert.full_name) : "?"}
              </div>
              <div className="min-w-0 flex-1">
                <p className="text-sm font-medium text-white truncate">
                  {expert?.full_name ?? "Loading..."}
                </p>
                <p className="text-xs text-gray-500 truncate">Admin</p>
              </div>
            </button>
            {showDropdown && (
              <>
                <div
                  className="fixed inset-0 z-40"
                  onClick={() => setShowDropdown(false)}
                />
                <div className="absolute bottom-full left-3 mb-2 w-48 bg-navy-light border border-gray-700 rounded-lg shadow-xl z-50 py-1">
                  <Link
                    href="/expert"
                    onClick={() => setShowDropdown(false)}
                    className="block px-3 py-2 text-sm text-gray-400 hover:text-white hover:bg-white/5 transition-colors"
                  >
                    Switch to Expert View
                  </Link>
                  <button
                    onClick={async () => {
                      await signOut();
                      router.push("/platform-admin/login");
                      router.refresh();
                    }}
                    className="w-full text-left px-3 py-2 text-sm text-gray-400 hover:text-white hover:bg-white/5 transition-colors"
                  >
                    Sign out
                  </button>
                </div>
              </>
            )}
          </div>
        )}
      </div>
    </aside>
  );
}

export default function PlatformAdminLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <ExpertAuthProvider>
      <div className="min-h-screen flex">
        <AdminSidebar />
        <main className="flex-1 overflow-auto">{children}</main>
      </div>
    </ExpertAuthProvider>
  );
}
